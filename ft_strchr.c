#include <stddef.h>
#include "libft.h"

/*
**	Locate the first occurrence of 'c' in 's'. Returns NULL if 'c' is not in s.
*/

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == (char)c)
			return ((char*)s);
		s++;
	}
	if (c)
		return (NULL);
	return ((void *)s);
}
